    <h1>**Star Wars Resumen:**</h1>

    <p>"Star Wars" es una icónica franquicia de ciencia ficción creada por George Lucas. La saga se desarrolla en una
        galaxia muy, muy lejana y abarca múltiples películas, series, libros y más. Aquí hay un resumen general de la
        trama principal:</p>

    <h3> **Trilogía Original (Episodios IV-VI):**</h3>
    <p>1. **Episodio IV - Una Nueva Esperanza (1977):** La historia sigue a Luke Skywalker, quien se embarca en una
        aventura después de recibir un mensaje de la Princesa Leia. Junto a Han Solo y Obi-Wan Kenobi, luchan contra el
        malvado Imperio Galáctico y su líder, Darth Vader. La película culmina en la destrucción de la Estrella de la
        Muerte.</p>

    <p>2. **Episodio V - El Imperio Contraataca (1980):** El Imperio contraataca, y los héroes enfrentan desafíos
        mayores. Luke entrena con Yoda mientras Han y Leia enfrentan peligros. La película culmina en una revelación
        impactante: Darth Vader es el padre de Luke.</p>

    <p> 3. **Episodio VI - El Retorno del Jedi (1983):** La Rebelión se prepara para enfrentarse al Imperio. Luke busca
        redimir a su padre mientras se libra una batalla en la luna de Endor y en la Estrella de la Muerte. El Emperador
        es derrotado, y Anakin Skywalker se redime, trayendo equilibrio a la Fuerza.</p>

    <h3>**Trilogía de Precuelas (Episodios I-III):**</h3>
    <p> 1. **Episodio I - La Amenaza Fantasma (1999):** La República está en crisis, y un joven Anakin Skywalker es
        descubierto por los Jedi. La Reina Amidala busca ayuda para su planeta Naboo, y se revela la amenaza de Darth
        Sidious. La película culmina en la liberación de Naboo y el entrenamiento de Anakin como aprendiz Jedi.</p>

    <p> 2. **Episodio II - El Ataque de los Clones (2002):** La galaxia se desliza hacia la guerra mientras Anakin y
        Padmé Amidala se enamoran. Los Jedi descubren un ejército de clones y la identidad de Darth Sidious. La película
        culmina en la creación del Gran Ejército Clon y la revelación de la existencia de un ejército separatista.</p>

    <p> 3. **Episodio III - La Venganza de los Sith (2005):** Anakin se vuelve hacia el lado oscuro, convirtiéndose en
        Darth Vader. La República se transforma en el Imperio, y los Jedi son exterminados. La película culmina en un
        duelo entre Anakin y Obi-Wan, y el nacimiento de los gemelos Luke y Leia.</p>

    <h3> **Trilogía Secuela (Episodios VII-IX):**</h3>
    <p> 1. **Episodio VII - El Despertar de la Fuerza (2015):** La Primera Orden amenaza la galaxia, y una joven
        chatarrera, Rey, se une a la resistencia. Busca a Luke Skywalker, quien ha desaparecido. La película culmina en
        la destrucción de la base Starkiller.</p>

    <p> 2. **Episodio VIII - Los Últimos Jedi (2017):** Rey busca entrenamiento de Luke mientras la Resistencia lucha
        contra la Primera Orden. La película culmina en la división entre Rey y Kylo Ren, y la Resistencia es diezmada.
    </p>

    <p>3. **Episodio IX - El Ascenso de Skywalker (2019):** Rey enfrenta a Palpatine, el regreso del Emperador,
        mientras la Resistencia se prepara para la batalla final. La película culmina en la derrota de Palpatine y el
        equilibrio final de la Fuerza.</p>

    <p>La saga "Star Wars" ha dejado un impacto duradero en la cultura popular, con personajes icónicos, batallas
        épicas y una mitología expansiva.</p>


