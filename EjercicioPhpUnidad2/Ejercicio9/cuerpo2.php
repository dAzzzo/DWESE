    <h1>**Regreso al Futuro Resumen:**</h1>

    <p>"Regreso al Futuro" es una icónica franquicia de ciencia ficción creada por Robert Zemeckis y Bob Gale. La saga
        sigue las aventuras de Marty McFly y el Doctor Emmett Brown a través del tiempo. Aquí hay un resumen general de la
        trama principal:</p>

    <h3>**Película 1:**</h3>
    <p>1. **"Regreso al Futuro" (1985):** Marty McFly, un adolescente, es accidentalmente enviado al pasado en el
        DeLorean modificado del Dr. Brown. En 1955, Marty conoce a sus padres jóvenes y trabaja junto al Doc para
        encontrar una manera de regresar al presente. La película culmina con el exitoso regreso a 1985.</p>

    <h3>**Película 2:**</h3>
    <p>2. **"Regreso al Futuro II" (1989):** Marty y el Doc viajan al futuro (2015) para resolver un problema en la
        familia McFly. Sin embargo, sus acciones tienen consecuencias inesperadas, y deben regresar a 1955 para corregir
        las alteraciones en la línea temporal. La película presenta un vistazo a diferentes líneas temporales.</p>

    <h3>**Película 3:**</h3>
    <p>3. **"Regreso al Futuro III" (1990):** La historia lleva a Marty y el Doc al Salvaje Oeste en 1885. El Doc se
        enamora, pero enfrentan desafíos mientras intentan reparar el DeLorean para regresar a 1985. La película culmina
        en un emocionante enfrentamiento y el regreso de Marty a su tiempo.</p>

    <p>La trilogía "Regreso al Futuro" combina aventuras, viajes en el tiempo y un elenco memorable para convertirse en
        un clásico del cine, apreciado por varias generaciones.</p>

